package com.type_casting;

public class implicit_class {

    public  static void main(String[] args){

        byte b=56;
        int i=b;
        System.out.println("Byte :" + b);
        System.out.println(("Int : "+ i));


        char c='A';
        int t=c;
        System.out.println("char :" + c);
        System.out.println(("Int : "+ t));

    }


    }
