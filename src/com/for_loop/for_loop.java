package com.for_loop;

public class for_loop {
    public static void main(String[] args){
        for(int i=5;i<=20;i++){
            System.out.println(i);
        }
        System.out.println("ascending order completed");
        for(int j=20;j>=5;j--){
            System.out.println(j);
        }
        System.out.println("descending order completed");
    }

}
